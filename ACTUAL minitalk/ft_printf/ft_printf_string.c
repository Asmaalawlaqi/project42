#include "ft_printf.h"

void	ft_putstr(char *str)
{
	if (str)
		write(1, str, ft_strlen(str));
}

int	string(char *str)
{
	if (!str)
	{
		ft_putstr("(null)");
		return (6);
	}
	ft_putstr(str);
	return (ft_strlen(str));
}
